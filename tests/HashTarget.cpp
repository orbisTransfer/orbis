#include <cstdint>
#include <cstdlib>
#include <cstring>
#include <limits>
#include "orbis/hash.h"
#include "OrbisCore/Difficulty.h"

using namespace std;
using Orbis::check_hash;

int main(int argc, char *argv[]) {
  Crypto::Hash h;
  for (uint64_t diff = 1;; diff += 1 + (diff >> 8)) {
    for (int b = 0; b < 256; b++) {
      memset(&h, b, sizeof(Crypto::Hash));
      if (check_hash(h, diff) != (b == 0 || diff <= 255 / b)) {
        return 1;
      }
      if (b > 0) {
        memset(&h, 0, sizeof(Crypto::Hash));
        ((char *) &h)[31] = b;
        if (check_hash(h, diff) != (diff <= 255 / b)) {
          return 1;
        }
      }
    }
    if (diff < numeric_limits<uint64_t>::max() / 256) {
      uint64_t val = 0;
      for (int i = 31; i >= 0; i--) {
        val = val * 256 + 255;
        ((char *) &h)[i] = static_cast<char>(val / diff);
        val %= diff;
      }
      if (check_hash(h, diff) != true) {
        return 1;
      }
      if (diff > 1) {
        for (int i = 0;; i++) {
          if (i >= 32) {
            abort();
          }
          if (++((char *) &h)[i] != 0) {
            break;
          }
        }
        if (check_hash(h, diff) != false) {
          return 1;
        }
      }
    }
    if (diff + 1 + (diff >> 8) < diff) {
      break;
    }
  }
  return 0;
}
